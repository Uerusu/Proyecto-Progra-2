    private void cargarTablaUsuarios(){
        
        String[] nombresColumnas = {"ID", "NOMBRE", "EMAIL", "USUARIO"};
        
        usuario = new Usuario();
        try {
            logicaUsuario.listarUsuario(usuario);
            //JOptionPane.showMessageDialog(null, usuario.getListaUsuarios().size());
            
            List<String[]> datosTabla = new java.util.ArrayList<>();
            for(Usuario usr : usuario.getListaUsuarios()){     
                String[] registro = new String[4];
                registro[0] = String.valueOf(usr.getId());
                registro[1] = usr.getNombre()+" "+usr.getApellido1()+" "+usr.getApellido2();
                registro[2] = usr.getEmail();
                registro[3]  = usr.getUsuario();
                datosTabla.add(registro);
            }
            
        UtilidadesTablas.configurarTablaConDatos(tblUsuarios, nombresColumnas, datosTabla);
  
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        limpiarCampos();

    }
    
    
