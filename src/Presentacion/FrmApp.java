    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
       
        int filaSeleccionada = tblUsuarios.getSelectedRow();
        
        txtId.setText(tblUsuarios.getValueAt(filaSeleccionada, 0).toString());
        
        String[] nombre = tblUsuarios.getValueAt(filaSeleccionada, 1).toString().split(" ");
        txtNombre.setText(nombre[0]);
        txtApellido1.setText(nombre[1]);
        txtApellido2.setText(nombre[2]);
        
        txtEmail.setText(tblUsuarios.getValueAt(filaSeleccionada, 2).toString());
        txtUsuario.setText(tblUsuarios.getValueAt(filaSeleccionada, 3).toString());
        
        
        
    }//GEN-LAST:event_tblUsuariosMouseClicked

